#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const projectRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..');
const localeDir = path.join(projectRoot, 'src', 'locale');
const outFile = path.join(projectRoot, 'src', 'i18n', 'generated-locales.ts');

function toImportName(code) {
  return `locale_${code.replace(/[^a-zA-Z0-9_]/g, '_')}`;
}

function isJsonLocaleFile(file) {
  return file.toLowerCase().endsWith('.json');
}

function toLocaleCode(file) {
  return file.replace(/\.json$/i, '').trim().toLowerCase();
}

function isLocaleDir(entry) {
  if (!entry.isDirectory()) return false;
  const code = entry.name.trim().toLowerCase();
  if (!isValidLocaleCode(code)) return false;
  const indexTs = path.join(localeDir, entry.name, 'index.ts');
  const indexTsx = path.join(localeDir, entry.name, 'index.tsx');
  return fs.existsSync(indexTs) || fs.existsSync(indexTsx);
}

function isValidLocaleCode(code) {
  return /^[a-z]{2,3}(?:-[a-z0-9]+)*$/i.test(code);
}

if (!fs.existsSync(localeDir)) {
  throw new Error(`Locale directory not found: ${localeDir}`);
}

const localeEntries = fs.readdirSync(localeDir, { withFileTypes: true });

const fileLocales = localeEntries
  .filter((entry) => entry.isFile() && isJsonLocaleFile(entry.name))
  .map((entry) => ({
    code: toLocaleCode(entry.name),
    importPath: `@/locale/${toLocaleCode(entry.name)}.json`,
  }));

const dirLocales = localeEntries
  .filter((entry) => isLocaleDir(entry))
  .map((entry) => ({
    code: entry.name.trim().toLowerCase(),
    importPath: `@/locale/${entry.name.trim().toLowerCase()}`,
  }));

const localeMap = new Map();
for (const item of [...fileLocales, ...dirLocales]) {
  if (isValidLocaleCode(item.code)) localeMap.set(item.code, item.importPath);
}

const locales = [...localeMap.keys()].sort((a, b) => a.localeCompare(b));

const imports = locales.map((code) => `import ${toImportName(code)} from '${localeMap.get(code)}';`);

const mapLines = locales.map((code) => `  '${code}': ${toImportName(code)},`);

const content = `// AUTO-GENERATED FILE. DO NOT EDIT.
// Generated by scripts/generate-locales-manifest.mjs

${imports.join('\n')}

export const LOCALE_MESSAGES = {
${mapLines.join('\n')}
} as const;

export type LocaleCode = keyof typeof LOCALE_MESSAGES;
export const LOCALE_CODES = Object.keys(LOCALE_MESSAGES) as LocaleCode[];
`;

const existing = fs.existsSync(outFile) ? fs.readFileSync(outFile, 'utf8') : '';
if (existing !== content) {
  fs.writeFileSync(outFile, content, 'utf8');
  console.log(`Generated ${path.relative(projectRoot, outFile)} with ${locales.length} locale(s).`);
} else {
  console.log(`No changes to ${path.relative(projectRoot, outFile)} (${locales.length} locale(s)).`);
}
